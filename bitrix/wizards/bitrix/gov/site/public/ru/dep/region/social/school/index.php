  <br />
 
  <br />
 Осуществлен переход на нормативное финансирование всех учреждений общего образования области. Это позволило увеличить финансирование отрасли с 1, 7 млрд. рублей в 2005 году <span style="font-weight: bold;">до 2,95 млрд. рублей в 2009 году</span>. 
  <br />
 
  <br />
