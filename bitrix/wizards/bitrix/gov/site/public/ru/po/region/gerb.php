<br />
 <b>Герб Ленинградской области</b> представляет собой изображение на геральдическом щите с отношением ширины к высоте 8:9 серебряного якоря, пересеченного золотым ключом на лазоревом (голубом) поле. В верхней части щита изображена серебряная зубчатая стена в перевязь на красном поле. 
<br />
 
<br />
 При воспроизведении герба Ленинградской области <b>должно быть обеспечено его цветовое и изобразительное соответствие</b> одному из оригиналов и описанию. 
<br />
 Допускается воспроизведение герба Ленинградской области: 
<br />
 
<ul> 
  <li>в виде цветного или одноцветного, объемного или графического изображения;</li>
 
  <li> в различной технике исполнения и из различных материалов;</li>
 
  <li> отличных от оригинала размеров с сохранением пропорций изображения.</li>
 </ul>
 В отдельных случаях допускается изображение основных элементов герба - якоря, ключа и зубчатой стены без геральдического щита, а также размещение отдельных элементов украшений вокруг геральдического щита. 
<br />
 
<br />
 <b>Изображение герба</b> Ленинградской области <b>помещается</b>: 
<br />
 
<ul> 
  <li>на бланках областных законов, актов Губернатора, Законодательного собрания и Правительства Ленинградской области;</li>
 
  <li>на зданиях Законодательного собрания и Правительства Ленинградской области, иных органов государственной власти и официальных представительств Ленинградской области;</li>
 
  <li> в залах заседаний Законодательного собрания и Правительства Ленинградской области;</li>
 
  <li> в служебных помещениях, занимаемых Губернатором Ленинградской области и Председателем Законодательного собрания Ленинградской области;</li>
 
  <li> на должностном нагрудном знаке Губернатора Ленинградской области, нагрудных знаках депутатов Законодательного собрания Ленинградской области;</li>
 
  <li> на официальных печатных изданиях органов законодательной и исполнительной власти Ленинградской области. </li>
 </ul>
 Иные случаи воспроизведения герба Ленинградской области, а также порядок использования изображения герба юридическими и физическими лицами в коммерческих, представительских целях устанавливаются Губернатором Ленинградской области.
