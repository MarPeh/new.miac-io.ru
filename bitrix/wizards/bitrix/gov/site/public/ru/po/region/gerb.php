 <span style="font-weight: bold;">Флаг Ленинградской области</span> представляет собой прямоугольное полотнище с отношением длины к ширине 3:2. В верхней части флага расположено белое поле, занимающее 2/3 его ширины. На белом поле в центре изображен герб Ленинградской области. Габаритная ширина герба на флаге Ленинградской области должна составлять 2/9 длины полотнища. В нижней части флага по всей длине расположены красная волнистая полоса, а над ней - голубая волнистая полоса, разделенные пополам белой волнистой полосой, составляющей 1/60 ширины полотнища флага. Обратная сторона флага является зеркальным отображением его лицевой стороны.
<br />

<br />
<span style="font-weight: bold;">Флаг </span>Ленинградской области <span style="font-weight: bold;">устанавливается </span>в залах заседаний Законодательного собрания и Правительства Ленинградской области, в рабочих кабинетах руководителей законодательной и исполнительной власти Ленинградской области, в рабочих кабинетах руководителей органов местного самоуправления (в комплексе с муниципальной символикой). 
