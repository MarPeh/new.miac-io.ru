		<td>1.1.5</td>
		<td><b>Капча</b>
			<p>Если цель нетекстового контента — подтверждение того, что к контенту обращается человек (а не компьютер), то пользователю доступна текстовая версия, которая идентифицирует и описывает цель этого нетекстового контента. Кроме того, предоставлена альтернативная форма капчи, которая использует альтернативные способы представления информации, доступные для других видов восприятия, для пользователей с различными видами инвалидности.</p>
			<p>На практике часть требования достаточно легко осуществима - большинство модулей для капчи в формах позволяют прослушать текст. Однако, сделать текстовый вывод того, что нужно ввести, для капчи невозможно (иначе робот легко прочтет). Как вариант - использование не просто набора символов, а простых вопросов, которые можно представить как графически, так и в виде текста.</p>
		</td>
		<td>Не используется</td>
