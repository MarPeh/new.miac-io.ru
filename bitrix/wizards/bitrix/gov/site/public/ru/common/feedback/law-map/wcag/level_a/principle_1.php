		<td><b>Тест</b>
			<p>Если нетекстовый контент является тестом или упражнением, которое невозможно представить в виде текста, то альтернативная текстовая версия дает, как минимум, краткое описание этого нетекстового контента.</p>
			<p>Речь про образовательные, обучающие программы, тесты и т.п. Для использующихся графического или аудио-материала используется расшифровка.</p>
