<h3>Принципы WCAG 2.0</h3>
<div>
	<ul>
		<li><b>Принцип 1. Воспринимаемость:</b> информация и компоненты пользовательского интерфейса должны быть представлены только в том виде, который могут воспринимать пользователи. </li>
		<li><b>Принцип 2. Управляемость:</b> компоненты пользовательского интерфейса и навигации должны быть управляемыми. (уровень А) </li>
		<li><b>Принцип 3. Понятность:</b> информация и операции пользовательского интерфейса должны быть понятными. </li>
		<li><b>Принцип 4. Надежность:</b> контент должен быть надежным в такой степени, которая требуется для его интерпретации широким кругом различных пользовательских приложений, включая ассистивные технологии.</li>
	</ul>
