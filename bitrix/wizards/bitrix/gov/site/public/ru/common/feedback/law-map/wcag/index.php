<h3>ГОСТ Р 52872-2012 &laquo;Интернет-ресурсы. Требования доступности для инвалидов по зрению&raquo; </h3>
<div>Настоящий стандарт распространяется на русскоязычные электронные ресурсы глобальной компьютерной сети Интернет (далее &ndash; интернет-ресурсы) и устанавливает общие требования доступности для инвалидов по зрению, использующих компьютер в качестве технического средства реабилитации. </div>
<div>
	<br />
</div>
<h3>Руководство WCAG2.0 (Web Content Accessibility Guidelines) </h3>
<div>Руководство по обеспечению доступности веб-контента (WCAG 2.0) дает рекомендации, как сделать веб-контент более доступным для широкого круга пользователей с ограниченными возможностями, включая инвалидов по зрению, слуху, опорно-двигательной системы, а также лиц с нарушениями функций речи, ментальной сферы и с неврологическими нарушениями. </div>
<div>Для удовлетворения потребностей различных групп пользователей в различных ситуациях Руководство определяет три уровня соответствия: А (низший), АА (средний) и ААА (наивысший). </div>
<div>«Руководство по обеспечению доступности веб-контента (WCAG) 2.0» является рекомендацией W3C от лица рабочей группы по разработке «Руководства по обеспечению доступности веб-контента» </div>
<div>Уполномоченный русский перевод: публикация 28 февраля 2013 <a href="http://www.w3.org/Translations/WCAG20-ru/">http://www.w3.org/Translations/WCAG20-ru/</a>  </div>
