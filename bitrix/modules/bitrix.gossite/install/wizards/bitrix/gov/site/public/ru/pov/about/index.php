<p><strong>Основные задачи отдела финансово-правовых вопросов</strong> 
  <br />
 </p>

<ul> 
  <li>Формирование и реализация единой государственной политики в сфере топливно-энергетического комплекса области, определение стратегии его развития в соответствии с государственной политикой и региональными особенностями. </li>
 
  <li>Содействие созданию правовых и экономических условий,обеспечивающих эффективное и рациональное использование бюджетных средств с целью обеспечения устойчивого и надежного функционирования организаций топливно-энергетического комплекса области. </li>
 
  <li>Участие, совместно со структурными подразделениями департамента и органами исполнительной власти области в формировании и реализации мероприятий, направленных на выполнение основных направлений развития отрасли, привлечение инвестиций в топливно-энергетический комплекс области. </li>
 
  <li>Осуществление контроля за движением финансовых ресурсов,находящихся в ведении департамента, формирование полной и достоверной информации о движении финансовых средств учреждения. </li>
 
  <li>Подготовка проектов нормативных правовых актов, приказов, распоряжений руководителя, связанных с исполнением полномочий департамента, исполнением законодательства о государственной гражданской службе, организация и обеспечение единой системы документооборота и порядка работы с документами.</li>
 </ul>
 
