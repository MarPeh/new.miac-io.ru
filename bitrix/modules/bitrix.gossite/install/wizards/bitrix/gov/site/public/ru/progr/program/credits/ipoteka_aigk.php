<p>Агентством разработана и внедрена двухуровневая система ипотечного кредитования, действующая по всей территории России. Участники системы получают возможность использовать финансовые ресурсы Агентства для выдачи ипотечных кредитов. Важной составляющей этой системы является тесное взаимодействие с Администрациями субъектов РФ и их уполномоченными ипотечными операторами, с которыми Агентство заключает трехсторонние договоры.</p>
 
<p>Агентство выкупает (рефинансирует) права требования по кредитам на приобретение жилых помещений и обеспеченных ипотекой, выданным банками и некредитными организациями по единым стандартам процедур выдачи. Эти стандарты разрабатываются и утверждаются Агентством с целью снижения рисков участников системы ипотечного жилищного кредитования, и обеспечения доступности ипотечного кредитования для населения Российской Федерации.</p>
 
<p>Деятельность Агентства контролируется Наблюдательным советом, в состав которого входят представители федеральных министерств, ведомств и служб, что обеспечивает эффективность, прозрачность и стабильность его работы.</p>

