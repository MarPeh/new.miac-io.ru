<p>Открытое акционерное общество &laquo;Агентство по ипотечному жилищному кредитованию&raquo; (Агентство, АИЖК) создано в 1997 году по решению Правительства РФ. 100% акций АИЖК принадлежит государству в лице Федерального агентства по управлению федеральным имуществом.</p>
 
<p>Главная задача Агентства заключается в реализации государственных программ по обеспечению доступным жильем и улучшения жилищных условий населения в рамках приоритетного национального проекта &laquo;Доступное и комфортное жилье &ndash; гражданам России&raquo;.</p>
 
<p>Цель работы Агентства по ипотечному жилищному кредитованию состоит в создании условий для того, чтобы все граждане России, вне зависимости от социального положения, образования, состава семьи, уровня доходов, профессии, вероисповедания, места проживания или регистрации имели равные возможности получения ипотечного кредита. Ипотечный жилищный кредит, <a href="#">выданный по cтандартам АИЖК</a>, доступен всем категориям граждан, выгоден и прост в оформлении.</p>
 
<p>Особое внимание АИЖК уделяется формированию рынка ипотеки в регионах, в которых уровень доходов на душу населения ниже, чем в среднем по стране, хуже развиты рынок жилья и рынок финансовых услуг.</p>
 
<p>Основные направления деятельности Агентства:</p>
 
<ul>
  <li> 
