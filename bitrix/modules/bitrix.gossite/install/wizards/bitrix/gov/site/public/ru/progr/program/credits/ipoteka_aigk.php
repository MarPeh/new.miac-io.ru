<p><a href="#"><strong>Стандарты процедур сопровождения ипотечных кредитов (займов)</strong></a>, закладных разработаны и введены с целью установления единых правил и требований, предъявляемых к порядку обслуживания и погашения ипотечных кредитов (займов), совершению юридически значимых действий с закладными и иными документами, входящими в состав кредитного дела, приобретенными Агентством, исполнению участниками инфраструктуры принятых на себя обязательств.</p>
 
<p>Стандарты разработаны Агентством на основании анализа и обобщения российского и зарубежного опыта ипотечного кредитования и включают в себя механизмы выдачи и рефинансирования ипотечных кредитов, типовые соглашения, а также требования ко всем участникам рынка (региональным операторам, банкам, оценочным, риэлторским и страховым компаниям).</p>
 
