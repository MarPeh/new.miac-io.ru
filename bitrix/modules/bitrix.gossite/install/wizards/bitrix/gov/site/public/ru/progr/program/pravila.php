<strong>Расчет размера социальной выплаты:</strong>
  <br />
Социальная выплата предоставляется в размере не менее:
  <br />
35% расчетной (средней) стоимости жилья для молодых семей не имеющих детей;
  <br />
40% расчетной (средней) стоимости жилья для молодых семей, имеющих 1 и более детей, а также для неполных молодых семей, состоящих из 1 молодого родителя и 1ребенка и более.
  <br />
Расчет размера социальной выплаты производится исходя из размера общей площади жилого помещения, установленного для семей разной численности, количества членов молодой семьи &ndash; участницы программы и норматива стоимости 1 кв.м. общей площади жилья по городскому округу город Воронеж.
  <br />
Норматив стоимость за 1 кв.м. ежеквартально устанавливается Приказом Министерства регионального развития РФ.
  <br />
Размер общей площади жилого помещения, с учетом которой определяется размер социальной выплаты, составляет:
  <br />
- для семьи численностью 2 человека &ndash; 42 кв.м.
  <br />
- для семьи численностью 3 и более человек &ndash; по 18 кв.м. на 1 человека. 
  <br />
