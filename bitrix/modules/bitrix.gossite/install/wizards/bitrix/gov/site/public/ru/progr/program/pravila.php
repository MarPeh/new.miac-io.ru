Социальная выплата предоставляется владельцу Свидетельства в безналичной форме путем зачисления соответствующих средств на его банковский счет, открытый в Центрально-Черноземном Банке Сбербанка России (<strong>г. Воронеж, ул. 9-го Января, д.28, ком.219</strong>).
  <br />
<strong>Перечень документов, необходимых для оформления банковского счета для обслуживания государственных программ</strong>:
  <br />
 
  <ul>
    <li>паспорт или иной документ, удостоверяющий личность владельца свидетельства;</li>
  
    <li>свидетельство о праве на получение субсидии на приобретение (строительство) жилья;</li>
  
    <li>карточку с образцами подписей формы N 0401026, заверенную банком.</li>
  
    <li>в случае открытия счета доверенным лицом: доверенность на распоряжение счетом и карточка с образцами подписей должны быть оформлены в нотариальном или ином установленном законом порядке.</li>
  </ul>
 Банк проверяет соответствие данных, указанных в свидетельстве, данным, содержащимся в документе, удостоверяющем личность владельца свидетельства, а также своевременность представления свидетельства в банк заключает с владельцем свидетельства договор о банковском счете для обслуживания государственных программ.
  <br />
В договоре банковского счета оговариваются основные условия обслуживания, порядок взаимоотношения банка и владельца свидетельства, на чье имя открыт банковский счет, а также порядок перевода средств с банковского счета. 
  <br />
Договор банковского счета заключается на срок, оставшийся до истечения срока действия свидетельства, и может быть расторгнут в течение срока действия договора по письменному заявлению распорядителя счета. В случае досрочного расторжения договора банковского счета (если на указанный счет не были зачислены средства, предоставляемые в качестве социальной выплаты) банк выдает распорядителю счета справку о расторжении договора банковского счета без перечисления средств социальной выплаты. Свидетельство, сданное в банк, после заключения договора банковского счета его владельцу не возвращается.
