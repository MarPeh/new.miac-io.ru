<strong>Выдача свидетельства:</strong>
  <br />
Для получения свидетельства о праве на получение социальной выплаты на приобретение (строительство) жилья молодая семья &#8213; претендент на получение социальной выплаты <strong>в двухмесячный срок</strong> с момента письменного уведомления направляет исполнителю программы заявление о выдаче свидетельства (произвольной формы) и пакет документов, необходимых и для участия в программе.
  <br />
При возникновении у молодой семьи &ndash; участницы программы обстоятельств, потребовавших замены выданного свидетельства, молодая семья представляет исполнителю программы, заявление о его замене с указанием обстоятельств, потребовавших такой замены, и приложением документов, подтверждающих эти обстоятельства. К указанным обстоятельствам относятся утрата (хищение) или порча свидетельства, а также уважительные причины, не позволившие молодой семье представить свидетельство в банк в установленный срок. В течение 30 дней с даты получения заявления выдаётся новое свидетельство, в котором указывается размер субсидии, предусмотренный в замененном свидетельстве.
  <br />
Срок действия свидетельства составляет <strong>не более 9 месяцев с даты выдачи</strong>, указанной в свидетельстве.
  <br />
Владелец свидетельства в течение <strong>2 месяцев с даты выдачи</strong> сдает его в банк. Свидетельство, представленное по истечении 2-месячного срока с даты его выдачи, банком не принимается.
