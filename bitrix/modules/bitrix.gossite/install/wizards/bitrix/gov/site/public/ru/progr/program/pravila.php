<strong>Оплата приобретаемого жилого помещения (создаваемого объекта индивидуального жилищного строительства)</strong>:
  <br />
Социальная выплата может быть использована на приобретение у любых физических и (или) юридических лиц одного (нескольких) жилого помещения (жилых помещений) или создание объекта индивидуального жилищного строительства, отвечающих установленным санитарным и техническим требованиям, благоустроенных применительно к условиям населенного пункта, выбранного для постоянного проживания, в котором приобретается (строится) жилое помещение.
  <br />
Приобретаемое жилое помещение (создаваемый объект индивидуального жилищного строительства) должно находиться на территории Воронежской области, и его общая площадь в расчете на каждого члена молодой семьи, учтенного при расчете размера социальной выплаты, не может быть меньше учетной нормы общей площади жилого помещения, установленной органами местного самоуправления в целях принятия граждан на учет в качестве нуждающихся в улучшении жилищных условий в месте приобретения (строительства) жилья.
  <br />
<em>Приобретаемое жилое помещение (создаваемый объект индивидуального жилищного строительства) оформляется в общую собственность всех членов молодой семьи, указанных в свидетельстве.</em>
